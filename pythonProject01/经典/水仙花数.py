# 水仙花数（也叫阿姆斯特朗数）是指：一个三位数，其各位数字的立方和等于该数本身。
# 例如：153 是水仙花数，因为 1³ + 5³ + 3³ = 1 + 125 + 27 = 153

for n in range(100,1000):
    # n = 153，那么i = 153 // 100 = 1
    i = n // 100
    # n = 153 → 153 // 10 = 15, 15 % 10 = 5，所以j = 5
    j = n //10 % 10
    # 153 % 10 = 3，所以k = 3
    k = n % 10
    if n == i*i*i + j*j*j + k*k*k:
        print(n)


